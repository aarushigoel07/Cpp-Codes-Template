/*A CPP program, that takes 2 integer numbers as input from user, calculates sum and difference , and prints the results.*/

#include<iostream>
using namespace std;

int main()
{
	int a,b;
	cout << "Enter 2 numbers :- " << endl;
	cin >> a >> b;
	cout << "Sum of the two numbers is : " << (a+b) << endl;
	cout << "Difference between the two numbers is : " << (a-b) << endl;
	return(0);
}

